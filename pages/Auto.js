import Head from "next/head";
import styles from "../styles/Home.module.css";
import React, { useState } from "react";
import TextField from "@mui/material/TextField";
import Autocomplete from "@mui/material/Autocomplete";
import axios from "axios";

const getNamesOne = async (str) => {
  try {
    //let searchableName = str.replace(/,/g, "");
    let url = "http://localhost:3000/api/autocompleteOne?name=" + str;

    let { data } = await axios.get(url);
    return data;
  } catch (error) {
    console.error(error);
  }
};

const getNamesTwo = async (str) => {
  try {
   // let searchableCity = str.replace(/,/g, "");
    let url = "http://localhost:3000/api/autocompleteTwo?name=" + str;
    //let url = "" + searchableCity;

    let { data } = await axios.get(url);
    return data;
  } catch (error) {
    console.error(error);
  }
};

export default function Home() {
  const [optionsOne, setOptionsOne] = useState([]);
  const [optionsTwo, setOptionsTwo] = useState([]);
  const [value, setValue] = useState("");

  const onChangeOne = async (e) => {
    if (e.target.value) {
      let data = await getNamesOne(e.target.value);
      setOptionsOne(data);
    }
  };

  const onChangeTwo = async (e) => {
    if (e.target.value) {
      let data = await getNamesTwo(e.target.value);
      setOptionsTwo(data);
    }
  };

  return (
    <div className={styles.container}>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <div style={{ marginTop: 50 }}>
        <Autocomplete
          freeSolo
          filterOptions={(x) => x}
          onChange={(e) => setValue(e.target.innerText)}
          options={optionsOne ? optionsOne.map((obj) => `${obj.name}:${obj.phone}:${obj.createdby}` ) : []}
          renderInput={(params) => (
            <TextField
              {...params}
              label="Search One"
              onChange={(e) => onChangeOne(e)}
            />
          )}
        />
        <Autocomplete
          freeSolo
          filterOptions={(x) => x}
          onChange={(e) => setValue(e.target.innerText)}
          options={optionsTwo ? optionsTwo.map((obj) => obj.name) : []}
          renderInput={(params) => (
            <TextField
              {...params}
              label="Search Two"
              onChange={(e) => onChangeTwo(e)}
            />
          )}
        />
      </div>
      <h1>{value}</h1>
    </div>
  );
}
